# imports the numpy library, which contains the log function
import numpy
# takes 2 user inputs, convert them to an integer type and assigning them to variables x and y
x = int(input("Please enter a value for x: "))
y = int(input("Please enter a value for y: "))
# prints the result of x to the power of y
print("x**y =", x**y)
# prints the result of log(x)
print("log(x) =", numpy.log2(x))

